def nonlinearity(x):
    # swish
    return x*torch.sigmoid(x)

class _NormalizeNP(torch.nn.GroupNorm):
    '''
    We found that the GroupNorm lightens the colors when running on higer resolution.
    And it also causes the color biases for each patch when running on patch.
    Thus, we first collect the mean and std for each patch and resize it to 
    the size of the whole image to obtain a normal color reconstrcution.
    '''

    def forward(self, z, _np=1):
        z = to_bnpnp(z, _np=_np)
        _b, _c, _h, _w = z.shape
        z = z.reshape(_b, self.num_groups, _c//self.num_groups, _h, _w)

        # std and mean for each local patch
        # we resize to std and mean to match the size of global image to 
        # ease the potential color bias in each patch
        _std, _mean = torch.std_mean(z, dim=(2,3,4), keepdim=True, unbiased=False)
        _mean = _mean.reshape(_b,self.num_groups,1,1)
        _mean = from_bnpnp(_mean, _np=_np) 
        # _mean = torch.nn.functional.interpolate(_mean,size=(_h*_np,_w*_np),mode="bilinear")
        _mean = torch.nn.functional.interpolate(_mean,size=(_h*_np,_w*_np),mode="bicubic")
        _mean = to_bnpnp(_mean, _np=_np).reshape(_b,self.num_groups,1,_h,_w)

        _std = _std.reshape(_b,self.num_groups,1,1)
        _std = from_bnpnp(_std, _np=_np)
        # _std = torch.nn.functional.interpolate(_std,size=(_h*_np,_w*_np),mode="bilinear")
        _std = torch.nn.functional.interpolate(_std,size=(_h*_np,_w*_np),mode="bicubic")
        _std = to_bnpnp(_std, _np=_np).reshape(_b,self.num_groups,1,_h,_w)

        z = (z-_mean)/(_std+self.eps)
        z = z.reshape(_b,_c,_h,_w) * self.weight.reshape(1,-1,1,1) + self.bias.reshape(1,-1,1,1)
        z = z.reshape(_b,_c,_h,_w)
        z = from_bnpnp(z, _np=_np)
        return z

def NormalizeNP(in_channels):
    return _NormalizeNP(num_groups=32, num_channels=in_channels, eps=1e-6, affine=True)

def Normalize(in_channels):
    return torch.nn.GroupNorm(num_groups=32, num_channels=in_channels, eps=1e-6, affine=True)

class Upsample(nn.Module):
    def __init__(self, in_channels, with_conv):
        super().__init__()
        self.with_conv = with_conv
        if self.with_conv:
            self.conv = torch.nn.Conv2d(in_channels,
                                        in_channels,
                                        kernel_size=3,
                                        stride=1,
                                        padding=1)

    def forward(self, x):
        x = torch.nn.functional.interpolate(x, scale_factor=2.0, mode="nearest")
        if self.with_conv:
            x = self.conv(x)
        return x


class Downsample(nn.Module):
    def __init__(self, in_channels, with_conv):
        super().__init__()
        self.with_conv = with_conv
        if self.with_conv:
            # no asymmetric padding in torch conv, must do it ourselves
            self.conv = torch.nn.Conv2d(in_channels,
                                        in_channels,
                                        kernel_size=3,
                                        stride=2,
                                        padding=0)

    def forward(self, x):
        if self.with_conv:
            pad = (0,1,0,1)
            x = torch.nn.functional.pad(x, pad, mode="constant", value=0)
            x = self.conv(x)
        else:
            x = torch.nn.functional.avg_pool2d(x, kernel_size=2, stride=2)
        return x


class ResnetBlock(nn.Module):
    def __init__(self, *, in_channels, out_channels=None, conv_shortcut=False,
                 dropout=0.0, temb_channels=512):
        super().__init__()
        self.in_channels = in_channels
        out_channels = in_channels if out_channels is None else out_channels
        self.out_channels = out_channels
        self.use_conv_shortcut = conv_shortcut

        self.norm1 = Normalize(in_channels)
        self.conv1 = torch.nn.Conv2d(in_channels,
                                     out_channels,
                                     kernel_size=3,
                                     stride=1,
                                     padding=1)
        if temb_channels > 0:
            self.temb_proj = torch.nn.Linear(temb_channels,
                                             out_channels)
        self.norm2 = Normalize(out_channels)
        self.dropout = torch.nn.Dropout(dropout)
        self.conv2 = torch.nn.Conv2d(out_channels,
                                     out_channels,
                                     kernel_size=3,
                                     stride=1,
                                     padding=1)
        if self.in_channels != self.out_channels:
            if self.use_conv_shortcut:
                self.conv_shortcut = torch.nn.Conv2d(in_channels,
                                                     out_channels,
                                                     kernel_size=3,
                                                     stride=1,
                                                     padding=1)
            else:
                self.nin_shortcut = torch.nn.Conv2d(in_channels,
                                                    out_channels,
                                                    kernel_size=1,
                                                    stride=1,
                                                    padding=0)

    def forward(self, x, temb=None):
        h = x
        h = self.norm1(h)
        h = nonlinearity(h)
        h = self.conv1(h)

        if temb is not None:
            h = h + self.temb_proj(nonlinearity(temb))[:,:,None,None]

        h = self.norm2(h)
        h = nonlinearity(h)
        h = self.dropout(h)
        h = self.conv2(h)

        if self.in_channels != self.out_channels:
            if self.use_conv_shortcut:
                x = self.conv_shortcut(x)
            else:
                x = self.nin_shortcut(x)

        return x+h

